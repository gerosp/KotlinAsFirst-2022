fun squareSequenceDigit(n: Int): Int {
    var current = 0
    var i = 0
    while (current < n) {
        i++
        current += digitNumber(i * i)
    }
    return if (current == n) {
        i * i % 10
    } else {
        (i * i / (10.toDouble().pow(current - n)).toInt()) % 10
    }

}

